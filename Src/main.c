/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
	/*OPCODE:
	 * A: Activate Alarm
	 * D: Deactivate Alarm
	 * NUM: Sensor ID
	 */
	char strOpcode = inboundRecive[0];
	char strNum = inboundRecive[1];
	sensornum = strNum - 48;
	if (strOpcode == 'A'){
		sensorAlarm[sensornum] = 1;
	}
	else if(strOpcode == 'D'){
		sensorAlarm[sensornum] = 0;
	}
	else if(strOpcode == 'S'){
		if(sensornum == 1){
			osTimerStart(uartSenderHandle,1000);
		}
		if(sensornum == 0){
			osTimerStop(uartSenderHandle);
		}
	}

}
